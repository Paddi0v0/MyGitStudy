/*******************************************************************************
 函数名称：    void CMP_IRQHandler(void)
 功能描述：    比较器中断处理函数
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2020/8/5      V1.0           Howlet Li          创建
 *******************************************************************************/
void CMP_IRQHandler(void)
{
	CMP_IF = BIT0 | BIT1;

	if (CMP_IF & (BIT1))
	{
		volatile u8 t_bi, t_bcnt;

		t_bcnt = 0;
		for (t_bi = 0; t_bi < 5; t_bi++)
		{
			if (SYS_AFE_CMP & BIT15) /* BIT14 CMP0 OUT Flag| BIT15 CMP1 OUT Flag */
			{
				t_bcnt++;
			}
		}

		if (t_bcnt > 3)
		{
			PWMOutputs(DISABLE);
			MCPWM_EIF = BIT4 | BIT5;
		}
	}
}

