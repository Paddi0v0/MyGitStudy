/*******************************************************************************
 函数名称：    void TIMER0_IRQHandler(void)
 功能描述：    TIMER0中断处理函数
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2020/8/5      V1.0           Howlet Li          创建
 *******************************************************************************/
void UTIMER0_IRQHandler(void)
{
	static u8 Input_Flag = 0;		       //捕获一个脉冲第一个上升沿标志位
	if (UTIMER_IF & Timer_IRQEna_Zero) //判断UTimer0是否发生过零中断
	{								                   //中断进入一次为0.5ms
		UTIMER_IF = Timer_IRQEna_Zero;   //清除UTimer中断标志位
		OverFlow += 1;
	}

	if (UTIMER_IF & Timer_IRQEna_CH0)  //判断UTimer0的CH0是否发生捕下降沿中断TIM0_CH0: P0.15
	{
		UTIMER_IF = Timer_IRQEna_CH0;    //清除UTimer中断标志位
		Invers_GPIO(GPIO0, GPIO_Pin_3);  //LED3反转
	}
	if (UTIMER_IF & Timer_IRQEna_CH1)  //判断UTimer0的CH1是否发生捕获上升沿中断 TIM0_CH1: P0.14
	{
		UTIMER_IF = Timer_IRQEna_CH1;    //清除UTimer中断标志位
		Input_Flag += 1;
		if (Input_Flag == 2)
		{
			InPut_CurrentTime = UTIMER_UNT0_CMP0;
			if (OverFlow != 0)
			{
				InPut_PulseTime = ((OverFlow - 1) * 48000 + 48000 - InPut_FrontTime + InPut_CurrentTime) / 96000; //单个脉冲的周期值(单位毫秒)
			}
			else
				InPut_PulseTime = InPut_CurrentTime - InPut_FrontTime; //单个脉冲的周期值(单位毫秒)
			Input_Flag = 1;
		}

		if (Input_Flag == 1)             //第一个上升沿标志位
		{
			InPut_FrontTime = UTIMER_UNT0_CMP0;
			OverFlow = 0;                  //清除捕获计数器溢出次数
		}
	}
}

