#include "hardware_config.h"
#include "delay.h"
#include "gpio_led.h"
#include "gpio_key.h"

/*******************************************************************************
 函数名称：    int main(void)
 功能描述：    主程序入口
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：    测试说明：
			   上电默认运行，
			   start按键启动
			   stop按键为停止
			   运行效果为：该主函数功能为按START键3个LED全亮，短按STOP键LED1灭
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 *******************************************************************************/
int main(void)
{
	u8 key = 0;
	Hardware_init();   /* 硬件初始化*/
	GPIO_LED(ALL_OFF); //初始化所有LED全灭
	while (1)
	{
		key = GPIO_KEY_Scan(0);
		GPIO_ResetBits(GPIO0, GPIO_Pin_15);
		switch (key)
		{
		case start_PRES:
			GPIO_LED(ALL_ON);
			break;
		case stop_PRES:
			LED1_OFF;
			break;
			
		}
		delay(0xFFFF);
	}
}

