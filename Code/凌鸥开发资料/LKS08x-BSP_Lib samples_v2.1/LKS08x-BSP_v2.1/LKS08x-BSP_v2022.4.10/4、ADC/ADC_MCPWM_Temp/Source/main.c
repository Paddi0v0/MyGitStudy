#include "hardware_config.h"
#include "delay.h"
#include "string.h"
extern void delay(u32 cnt);
/*******************************************************************************
 函数名称：    int main(void)
 功能描述：    主程序入口
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：    测试说明：
			   通电运行。
			   运行效果为：程序烧录后，进入Debug模式，查看TEMP变量，可读取当前芯片温度℃

 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2021/8/27                      HMG
 *******************************************************************************/
uint32_t a = 0, b = 0;
uint32_t TEMP = 0;
extern int Voltage0_Value1;
int main(void)
{
	Hardware_init();   /* 硬件初始化 */
	while (1)
	{
		SYS_AFE_REG5 |= BIT13;				   //开启温度传感器
		Read_More_Flash(0x00000398, 1, &a, 1); //读NVR温度校正系数a程序
		Read_More_Flash(0x0000039C, 1, &b, 1); //读NVR温度校正系数b内程序
		TEMP = (-((Voltage0_Value1 >> 4) * a / 10000)) + (b / 10);//当前芯片温度单位℃
		delay(0xffff0);
	}
}

