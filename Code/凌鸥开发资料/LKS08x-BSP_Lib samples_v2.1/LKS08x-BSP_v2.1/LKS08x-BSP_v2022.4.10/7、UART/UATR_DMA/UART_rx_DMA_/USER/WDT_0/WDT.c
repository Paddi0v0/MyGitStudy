#include "WDT.h"

/*******************************************************************************
 函数名称：    void WDT_Init(void)
 功能描述：    WDT初始化函数
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2021/2/25      V1.0                               创建
 *******************************************************************************/
void WDT_Init(void)
{
    SYS_RST_CFG |= BIT0;//看门狗使能
    SYS_WDT_CLR = WDT_CLR2;
}
/*******************************************************************************
 函数名称：    void WDT_Reset(void)
 功能描述：    WDT计数器复位函数
 输入参数：    无
 输出参数：    无
 返 回 值：    无
 其它说明：
 修改日期      版本号          修改人            修改内容
 -----------------------------------------------------------------------------
 2021/2/25      V1.0                               创建
 *******************************************************************************/
void WDT_Reset(void)
{
  SYS_WDT_CLR = WDT_CLRFeed;
}

