u8 UART_Value = 0; //串口接收1byte数据缓冲区
char string[] = "Hellow word !\r";
int main(void)
{
	Hardware_init(); /* 硬件初始化 */
	while (1)
	{
		if (UART0_IF & UART_IF_SendOver) //发送完成中断
		{
			UART0_IF = UART_IF_SendOver;
			UART_Flag = 0;
		}
		if (UART0_IF & UART_IF_RcvOver) //接收完成中断
		{
			UART0_IF = UART_IF_RcvOver;
			UART_Value = UART0->BUFF;
			UART0->BUFF = UART_Value & 0xff;
		}
		UART_SendData(UART0, 0XAA); //发送数据
		delay(0X2FFFF);				//延时
	}
}

